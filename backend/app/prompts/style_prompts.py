"""自定义写作风格提取 Prompt"""


def build_style_extraction_prompt(samples_text: str) -> str:
    return f"""你是资深小说编辑。阅读以下作者样本，归纳其固定的写作风格，输出一份能让 AI 模仿该风格的"风格画像"。

输出格式（中文、纯文本、不要使用 markdown 标题，总长度 600-900 字）：

一、语言与句式：句式长短、文白比例、节奏感、标点习惯。
二、描写偏好：景物/动作/心理/战斗各自的占比与处理手法。
三、对话风格与人物口吻：对话密度、语气词、称谓、是否有旁白打断。
四、叙事视角与节奏感：第几人称、POV 切换方式、张弛分布。
五、典型词汇与修辞标记：作者偏爱的意象、成语、比喻模式、独特表达。
六、情绪基调与氛围：整体情绪色彩（冷峻/热血/阴郁/诙谐等）。
七、模仿要点（3-5 条给 AI 的硬性指令）：如"多用短句"、"避免系统音效提示"、"每章结尾必有一句白描收束"等，必须是可执行的写作约束。

注意事项：
- 只总结**形式层面**的风格，不要复述或分析情节、世界观、人物。
- 不要出现"作者"、"样本"、"原文"等元字样。
- 若样本风格存在矛盾或多变，选出现频率最高的那种。
- 直接从"一、语言与句式"开始写，不要前言也不要总结。

【样本】
{samples_text}
"""
