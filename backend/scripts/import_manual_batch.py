async def main():
    now = datetime.now(timezone.utc).isoformat()
    async with aiosqlite.connect(DB) as db:
        cur = await db.execute("SELECT title FROM knowledge_items")
        existing = {row[0] for row in await cur.fetchall()}
        inserted = 0
        for it in ITEMS:
            if it["title"] in existing:
                continue
            await db.execute(
                "INSERT INTO knowledge_items (id,category,sub_type,title,content,tags,is_builtin,created_at,updated_at) VALUES (?,?,?,?,?,?,1,?,?)",
                (str(uuid.uuid4()), it["category"], it["sub_type"], it["title"], it["content"],
                 json.dumps(it["tags"], ensure_ascii=False), now, now))
            inserted += 1
        await db.commit()
        cur = await db.execute("SELECT COUNT(*) FROM knowledge_items")
        total = (await cur.fetchone())[0]
        cur = await db.execute("SELECT category, COUNT(*) FROM knowledge_items GROUP BY category ORDER BY COUNT(*) DESC")
        cats = await cur.fetchall()
        print(f"手动补充: 新增 {inserted} 条")
        print(f"数据库总计: {total} 条")
        for cat, cnt in cats:
            print(f"  {cat}: {cnt}")


if __name__ == "__main__":
    asyncio.run(main())
